package jmind.redis;

/**
 * Exception thrown when the thread executing a redis command is
 * interrupted.
 *
 * @author wbxie
 */
@SuppressWarnings("serial")
public class RedisCmdInterruptedException extends RedisException {
    public RedisCmdInterruptedException(Throwable e) {
        super("Command interrupted", e);
    }
}
